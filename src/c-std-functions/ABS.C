/*************************************************
*
* abs - absolute value
*
* Inputs:
*	j	- value to calculate abs of
*
* Returns:
*	absloute value of j
*
*************************************************/
int abs(int j)
{
  if (j < 0)
    return(-j);
  else 
    return(j);
}
